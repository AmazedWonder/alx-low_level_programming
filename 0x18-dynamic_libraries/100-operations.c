#include <stdio.h>

/**
 * add - function adds two integers
 *
 * @a: the first integer
 * @b: the second integer
 *
 * Return: sum of the two integers
 */
int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - function subtracts two integers
 *
 * @a: the first integer
 * @b: the second integer
 *
 * Return: difference between two integers
 */
int sub(int a, int b)
{
	return (a - b);
}

/**
 * mul - function multiplies two integers
 *
 * @a: the first integer
 * @b: the second integer
 *
 * Return: product of the two integers
 */
int mul(int a, int b)
{
	return (a * b);
}

/**
 * div - function divides two integers
 *
 * @a: the first integer
 * @b: the second integer
 *
 * Return: quotient of the two integers
 */
int div(int a, int b)
{
	return (a / b);
}

/**
 * mod - function computes the modulo of two integers
 *
 * @a: the first integer
 * @b: the second integer
 *
 * Return: modulo/remainder of two integers
 */
int mod(int a, int b)
{
	return (a % b);
}
